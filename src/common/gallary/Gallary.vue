    <div class="container" @click="handleBannerClick">
      <div class="wrapper">
        <swiper :options="swiperOptions">
            <swiper-slide v-for="(item, index) of imgs" :key="index">
              <img class="gallary-img" :src="item" />
            </swiper-slide>
            <div class="swiper-pagination"  slot="pagination"></div>
        </swiper>
      </div>
    </div>
</template>

<script>
export default {
  name: 'CommonGallary',
  props: {
    imgs: {
      type: Array,
      default () {
        return []
      }
    }
  },
  data () {
    return {
      swiperOptions: {
        pagination: '.swiper-pagination',
        // 图片底部 1/2 效果设置
        paginationType: 'fraction',
        // 当dom元素变化进行刷新,解决条件显示后宽度计算问题
        observeParents: true,
        observer: true
      }
    }
  },
  methods: {
    handleBannerClick () {
      this.$emit('close')
    }
  }
}
</script>

